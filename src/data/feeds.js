/**
 * Smart Rule-Based Classification Model
 * Verifies if an article genuinely belongs to a category using a weighted scoring system.
 */
export const isArticleRelevant = (article, category) => {
    // Aggregator feeds like Google News need strict keyword filtering.
    // Curated feeds (ET, Livemint, etc.) are trusted to prevent empty categories, EXCEPT for Stocks,
    // where we want to be strict to avoid general business news bleeding in.
    const isAggregator = article.sourceName && article.sourceName.toLowerCase().includes('google');
    // If it is not Stocks AND not an aggregator feed (Google Search), trust it as relevant.
    if (!isAggregator && category !== 'Stocks') return true;

    const keywords = CATEGORY_KEYWORDS[category] || [];
    if (keywords.length === 0) return true;

    const title = (article.title || '').toLowerCase();
    const desc = (article.shortDescription || '').toLowerCase();
    const content = (article.fullContent || '').toLowerCase();

    let score = 0;

    // Core keywords check with weighted scoring
    keywords.forEach(kw => {
        const word = kw.toLowerCase();

        // Exact title match is a very strong indicator, description moderate, content weak
        // Use exact word boundaries for single English words to prevent false positives.
        // For phrases or non-English characters (like Hindi), fallback to simple includes.
        const useRegex = !(/[^\w\s]/.test(word)) && !word.includes(' ');

        if (useRegex) {
            const regex = new RegExp(`\\b${word}\\b`, 'i');
            if (regex.test(title)) score += 3;
            else if (regex.test(desc)) score += 2;
            else if (regex.test(content)) score += 1;
        } else {
            if (title.includes(word)) score += 3;
            else if (desc.includes(word)) score += 2;
            else if (content.includes(word)) score += 1;
        }
    });

    // Require a minimum confidence score to be classified as relevant
    return score > 0;
};

/**
 * Re-orders a collection of articles to ensure source variety.
 * It grouping articles by source and interleaves them.
 */
export const diversifySources = (articles, limit = 0) => {
    if (!articles || articles.length === 0) return [];

    const sourceGroups = {};
    articles.forEach(article => {
        const source = article.sourceName || 'Unknown';
        if (!sourceGroups[source]) sourceGroups[source] = [];
        sourceGroups[source].push(article);
    });

    const sources = Object.keys(sourceGroups);
    const result = [];
    let maxItems = 0;
    sources.forEach(s => { maxItems = Math.max(maxItems, sourceGroups[s].length); });

    // Interleave sources
    for (let i = 0; i < maxItems; i++) {
        sources.forEach(source => {
            if (sourceGroups[source][i]) {
                result.push(sourceGroups[source][i]);
            }
        });
        if (limit > 0 && result.length >= limit) break;
    }

    return limit > 0 ? result.slice(0, limit) : result;
};

/**
 * Checks if an article contains blocked keywords or is too old (stale).
 */
export const isBlocked = (article) => {
    const title = (article.title || '').toLowerCase();
    const desc = (article.shortDescription || '').toLowerCase();
    const text = `${title} ${desc}`;

    // 1. Script block: Filter out non-English/Hindi scripts (e.g., Korean, Chinese, Japanese)
    // Fix: Use single backslash for Unicode escapes in regex literal
    const cjkRegex = /[\u3040-\u30ff\u3400-\u4dbf\u4e00-\u9fff\uf900-\ufaff\uff00-\uffef\uac00-\ud7af]/;
    if (cjkRegex.test(article.title) || cjkRegex.test(article.shortDescription)) return true;

    // 1b. Robust check: Ensure the article is either English or Hindi
    const hasEnglish = /[a-zA-Z]/.test(title);
    const hasHindi = /[\u0900-\u097F]/.test(title);
    if (!hasEnglish && !hasHindi) return true; // Reject articles with NO English or Hindi characters in title

    // 2. Keyword block
    if (BLOCKED_KEYWORDS.some(kw => text.includes(kw.toLowerCase()))) return true;

    // 3. Staleness block (Strictly 24 hours)
    if (!article.pubDate) return true; // Block articles with no date (usually ads)

    const pubDate = new Date(article.pubDate);
    if (!isNaN(pubDate.getTime())) {
        const threshold = new Date();
        const maxStalenessHours = 72; // Allow up to 3 days to support slower feeds (Economy/Euro/US)

        threshold.setHours(threshold.getHours() - maxStalenessHours);
        if (pubDate < threshold) return true;
    } else {
        return true; // Invalid date format
    }

    return false;
};

